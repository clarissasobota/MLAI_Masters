'''
    Add two numbers together and print result
        Input:
            first: First number
            second: Second number

'''
def add_numbers(first,second):
    print('\n\n================================================')
    print('\nThe sum of the two numbers is: ' + str(first + second))


'''
    Subtract the two numbers and print results
'''
def subtract_numbers(first,second):
    print('The first number minus the second number is: ' + str(first - second))
