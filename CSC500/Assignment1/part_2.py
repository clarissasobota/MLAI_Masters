'''
    Multiply two numbers together and print result
        Input:
            first: First number
            second: Second number

'''
def multiply_numbers(first,second):
    print('\n\n================================================')
    print('\nThe product of the two numbers is: ' + str(first * second))


'''
    Divid the two numbers and print results
'''
def divide_numbers(first,second):
    print('The first number divided by the second number is: ' + str(first / second))
    print('The second number divided by the first number is: ' + str(second / first))
    print('\n================================================')